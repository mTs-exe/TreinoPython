# Operadores Aritméticos

'''

O sinal "+" significa adição
O sinal "-" significa subtração
O sinal "*" significa multiplicação
O sinal "/" significa divisão
O sinal "**" significa potência
O sinal "//" significa Inteira
O sinal "%" significa Resto da Divisão

'''

# Ordem de precedência

'''

1- ()
2- **
3- *, /, //, %
4- +, -

'''

# Exemplo

print(5+3*2)
print(3*5+4**2)
print(3*(5+4)**2)